       * Therefore, when TurboModules are enabled, we must manually create this
       * NativeModule.
       */
       [_turboModuleManager moduleForName:"DevMenu"];
    }
  }

  return RCTAppSetupDefaultJsExecutorFactory(bridge, _turboModuleManager);
}

